Friday is the weekly day of communal worship in Islam. The weekly
convened Friday Prayer is the most important service. The Friday Prayer is marked by
the following features:<br><br>
• It falls in the same time as the noon prayer which it replaces.<br><br>
• It must be performed in a congregation led by a prayer leader, an ‘Imam.’ It
can not be offered individually. Muslims in the West try to arrange their schedules to
allow them time to attend the prayer.<br><br>
• Rather than a day of rest like the Sabbath, Friday is a day of devotion and extra
worship. A Muslim is allowed normal work on Friday as on any other day of the week.
They may proceed with their usual activities, but they must break for the Friday
prayer. After the worship is over, they can resume their mundane activities.<br><br>
• Typically, the Friday Prayer is performed in a mosque, if available. Sometimes, due
to unavailability of a mosque, it may be offered at a rented facility, park,
etc.<br><br>
• When the time for prayer comes, the Adhan is pronounced The Imam then stands
facing the audience and delivers his sermon (known as khutba in Arabic), an essential
part of the service of which its attendance is required. While the Imam is talking,
everyone present listens to the sermon quietly till the end. Most Imams in the West
will deliver the sermon in English, but some deliver it in Arabic. Those who deliver it
in Arabic usually deliver a short speech in the local language before the
service.<br><br>
• There are two sermons delivered, one distinguished from the other by a brief sitting
of the Imam. The sermon is commenced with words of praise of God and prayers of
blessing for Prophet Muhammad, may the mercy and blessings of God be upon
him.<br><br>
• After the sermon, the prayer is offered under the leadership of the Imam who recites
the Fatiha and the other Quranic passage in an audible voice. When this is done, the
prayer is completed. Special, large congregational prayers, which include a sermon,
are also offered at late morning on the two days of festivity. One of them is
immediately following the month of fasting, Ramadan, and the other after the
pilgrimage, or hajj. Although not religiously mandated, individual devotional prayers,
especially during the night, are emphasized and are a common practice among pious
Muslims
