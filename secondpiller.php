Having specific times each day to be close to God helps Muslims remain aware of the
importance of their faith, and the role it plays in every part of life. Muslims start their
day by cleaning themselves and then standing before their Lord in prayer. The prayers
consist of recitations from the Quran in Arabic and a sequence of movements:
standing, bowing, prostrating, and sitting. All recitations and movements express
submission, humility, and homage to God. The various postures Muslims assume
during their prayers capture the spirit of submission; the words remind them of their
commitments to God. The prayer also reminds one of belief in the Day of Judgment
and of the fact that one has to appear before his or her Creator and give an account of
their entire life. This is how a Muslim starts their day. In the course of the day,
Muslims dissociate themselves form their worldly engagements for a few moments
and stand before God. This brings to mind once again the real purpose of
life.<br><br> These prayers serve as a constant reminder throughout the day to help
keep believers mindful of God in the daily stress of work, family, and distractions of
life. Prayer strengthens faith, dependence on God, and puts daily life within the
perspective of life to come after death and the last judgment. As they prepare to pray,
Muslims face Mecca, the holy city that houses the Kaaba (the ancient place of
worship built by Abraham and his son Ishmael). At the end of the prayer, the shahada
(testimony of faith) is recited, and the greeting of peace, “Peace be upon all of you
and the mercy and blessings of God,” is repeated twice.<br><br> Though individual
performance of salah is permissible, collective worship in the mosque has special
merit and Muslims are encouraged to perform certain salah with others. With their
faces turned in the direction of the Kaaba in Mecca, the worshipers align themselves
in parallel rows behind the imam, or prayer leader, who directs them as they execute
the physical postures coupled with Quran recitations. In many Muslim countries, the
“call to prayer,” or ‘Adhan,’ echo out across the rooftops. Aided by a megaphone the
