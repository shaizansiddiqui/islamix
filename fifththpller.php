<h2 style="color: rgb(0,200,0);" class="text-center"> Compulsory Charity </h2>
<p style="color: black;font-size: 20px ;text-align: justify;
display: flex;
flex-wrap: wrap;
align-content: center;margin-left: 100px;margin-right: 100px;">Charity is not just recommended by Islam, it is required of every financially stable
Muslim. Giving charity to those who deserve it is part of Muslim character and one of
the Five Pillars of Islamic practice. Zakat is viewed as “compulsory charity”; it is an
obligation for those who have received their wealth from God to respond to those
members of the community in need. Devoid of sentiments of universal love, some
people know only to hoard wealth and to add to it by lending it out on interest. Islam’s
teachings are the very antithesis of this attitude. Islam encourages the sharing of
wealth with others and helps people to stand on their own and become productive
members of the society.<br><br>
In Arabic it is known as zakat which literally means “purification”, because zakat is
considered to purify one’s heart of greed. Love of wealth is natural and it takes firm
belief in God for a person to part with some of his wealth. Zakat must be paid on
different categories of property — gold, silver, money; livestock; agricultural produce;
and business commodities — and is payable each year after one year’s possession. It
requires an annual contribution of 2.5 percent of an individual’s wealth and
assets.<br><br> Like prayer, which is both an individual and communal
responsibility, zakat expresses a Muslim’s worship of and thanksgiving to God by
supporting those in need. In Islam, the true owner of things is not man, but God.
Acquisition of wealth for its own sake, or so that it may increase a man’s worth, is
condemned. Mere acquisition of wealth counts for nothing in the sight of God. It does
not give man any merit in this life or in the hereafter. Islam teaches that people should
acquire wealth with the intention of spending it on their own needs and the needs of
others. <br><br>
“‘Man’, said the Prophet, ‘says: My wealth! My wealth!’ Have you not any wealth
except that which you give as alms and thus preserve, wear and tatter, eat and use
up?”<br><br>
The whole concept of wealth is considered in Islam as a gift from God. God, who
provided it to the person, made a portion of it for the poor, so the poor have a right
over one’s wealth. Zakat reminds Muslims that everything they have belongs to God.
People are given their wealth as a trust from God, and zakat is intended to free
Muslims from the love of money. The money paid in zakat is not something God
needs or receives. He is above any type of dependency. God, in His boundless mercy,
promises rewards for helping those in need with one basic condition that zakat be paid
in the name of God; one should not expect or demand any worldly gains from the
beneficiaries nor aim at making one’s names as a philanthropist. The feelings of a
beneficiary should not be hurt by making him feel inferior or reminding him of the
assistance.<br><br>
Money given as zakat can only be used for certain specific things. Islamic Law
stipulates that alms are to be used to support the poor and the needy, to free slaves and
debtors, as specifically mentioned in the Quran (9:60). Zakat, which developed
fourteen hundred years ago, functions as a form of social security in a Muslim
society.<br><br>
Neither Jewish nor Christian scriptures praise slave manumission by raising it to
worship. Indeed, Islam is unique in world religions in requiring the faithful to
financially help slaves win their freedom and has raised the manumission of a slave to
an act of worship - if it is done to please God.<br><br>
Under the caliphates, the collection and expenditure of zakat was a function of the
state. In the contemporary Muslim world, it has been left up to the individual, except
in some countries in which the state fulfills that role to some degree. Most Muslims in
the West disperse zakat through Islamic charities, mosques, or directly giving to the poor. Money is not collected during religious services or via collection plates, but
some mosques keep a drop box for those who wish it to distribute zakat on their
behalf. Unlike the zakat, Giving other forms of charity in private, even in secret, is
considered better, in order to keep one’s intention purely for the God.<br><br>
Apart from zakat, the Quran and Hadeeth (sayings and actions of the Prophet
Muhammad, may the mercy and blessings of God be upon him) also stress sadaqah,
or voluntary almsgiving, which is intended for the needy. The Quran emphasizes
feeding the hungry, clothing the naked, helping those who are in need, and the more
one helps, the more God helps the person, and the more one gives, the more God
gives the person. One feels he is taking care of others and God is taking care of him.
