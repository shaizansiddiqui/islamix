Fasting is not unique to the Muslims. It has been practiced for centuries in connection
with religious ceremonies by Christians, Jews, Confucianists, Hindus, Taoists, and
Jains. God mentions this fact in the Quran:<br><br>
“O you who believe, fasting is prescribed for you as it was prescribed for those
before you, that you may develop God-consciousness.” (Quran 2:183)<br><br>
Some Native American societies fasted to avert catastrophe or to serve as penance for
sin. Native North Americans held tribal fasts to avert threatening disasters. The Native
Americans of Mexico and the Incas of Peru observed penitential fasts to appease their
gods. Past nations of the Old World, such as the Assyrians and the Babylonians,
observed fasting as a form of penance. Jews observe fasting as a form of penitence
and purification annually on the Day of Atonement or Yom Kippur. On this day
neither food nor drink is permitted.<br><br>
Early Christians associated fasting with penitence and purification. During the first
two centuries of its existence, the Christian church established fasting as a voluntary
preparation for receiving the sacraments of Holy Communion and baptism and for the
ordination of priests. Later, these fasts were made obligatory, as others days were
subsequently added. In the 6th century, the Lenten fast was expanded to 40 days, on
each of which only one meal was permitted. After the Reformation, fasting was
retained by most Protestant churches and was made optional in some cases. Stricter
Protestants, however, condemned not only the festivals of the church, but its
traditional fasts as well.<br><br>
In the Roman Catholic Church, fasting may involve partial abstinence from food and
drink or total abstinence. The Roman Catholic days of fasting are Ash Wednesday and
Good Friday. In the United States, fasting is observed mostly by Episcopalians and
Lutherans among Protestants, by Orthodox and Conservative Jews, and by Roman
Catholics.<br><br>
Fasting took another form in the West: the hunger strike, a form of fasting, which in
modern times has become a political weapon after being popularized by Mohandas
Gandhi, leader of the struggle for India’s freedom, who undertook fasts to compel his
followers to obey his precept of nonviolence.<br><br>
Islam is the only religion that has retained the outward and spiritual dimensions of
fasting throughout centuries. Selfish motives and desires of the base self alienate a
man from his Creator. The most unruly human emotions are pride, avarice, gluttony,
lust, envy, and anger. These emotions by their nature are not easy to control, thus a
person must strive hard to discipline them. Muslims fast to purify their soul, it puts a
bridle on the most uncontrolled, savage human emotions. People have gone to two
extremes with regard to them. Some let these emotions steer their life which lead to
barbarism among the ancients, and crass materialism of consumer cultures in modern
times. Others tried to deprive themselves completely of these human traits, which in
turn led to monasticism.<br>
The fourth Pillar of Islam, the Fast of Ramadan, occurs once each year during the 9th
lunar month, the month of Ramadan, the ninth month of the Islamic calendar in which:
<br>
“…the Quran was sent down as a guidance for the people.” (Quran 2:185)<br><br>
God in His infinite mercy has exempt the ill, travelers, and others who are unable
from fasting Ramadan.<br>
Fasting helps Muslims develop self-control, gain a better understanding of God’s
gifts and greater compassion towards the deprived. Fasting in Islam involves
abstaining from all bodily pleasures between dawn and sunset. Not only is food
forbidden, but also any sexual activity. All things which are regarded as prohibited is even more so in this month, due to its sacredness.. Each and every moment during the
fast, a person suppresses their passions and desires in loving obedience to God. This
consciousness of duty and the spirit of patience helps in strengthening our faith.
Fasting helps a person gain self-control. A person who abstains from permissible
things like food and drink is likely to feel conscious of his sins. A heightened sense of
spirituality helps break the habits of lying, staring with lust at the opposite sex,
gossiping, and wasting time. Staying hungry and thirsty for just a day’s portion makes
one feel the misery of the 800 million who go hungry or the one in ten households in
the US, for example, that are living with hunger or are at risk of hunger. After all,
why would anyone care about starvation if one has never felt its pangs oneself? One
can see why Ramadan is also a month of charity and giving.<br><br>
At dusk, the fast is broken with a light meal popularly referred to as iftaar. Families
and friends share a special late evening meal together, often including special foods
and sweets served only at this time of the year. Many go to the mosque for the
evening prayer, followed by special pra yers recited only during Ramadan. Some will
recite the entire Quran as a special act of piety, and public recitations of the Quran can
be heard throughout the evening. Families rise before dawn to take their first meal of
the day, which sustains them until sunset. Near the end of Ramadan Muslims
commemorate the “Night of Power” when the Quran was revealed. The month of
Ramadan ends with one of the two major Islamic celebrations, the Feast of the
Breaking of the Fast, called Eid al-Fitr. On this day, Muslims joyfully celebrate the
completion of Ramadan and customarily distribute gifts to children. Muslims are also
obliged to help the poor join in the spirit of relaxation and enjoyment by distributing
zakat-ul-fitr, a special and obligatory act of charity in the form of staple foodstuff, in
order that all may enjoy the general euphoria of the day.
