<h2 style="color: rgb(0,200,0);" class="text-center">The Pilgrimage (Hajj) </h2>
<p style="color: black;font-size: 20px ;text-align: justify;
display: flex;
flex-wrap: wrap;
align-content: center;margin-left: 100px;margin-right: 100px;">The Hajj (pilgrimage to Mecca) is the fifth of the fundamental Muslim practices and
institutions known as the five pillars of Islam. Pilgrimage is not undertaken in Islam
to the shrines of saints, to monasteries for help from holy men, or to sights where
miracles are supposed to have occurred, even though we may see many Muslims do
this. Pilgrimage is made to the Kaaba, found in the sacred city of Mecca in Saudia, the
‘House of God,’ whose sanctity rests in that the Prophet Abraham built it for the
worship of God. God rewarded him by attributing the House to himself, in essence
honoring it, and by making it the devotional epicenter which all Muslims face when
offering the prayers (salah). The rites of pilgrimage are performed today exactly as
did by Abraham, and after him by Prophet Muhammad, may God praise
them.<br><br>
Pilgrimage is viewed as a particularly meritorious activity. Pilgrimage serves as a
penance - the ultimate forgiveness for sins, devotion, and intense spirituality. The
pilgrimage to Mecca, the most sacred city in Islam, is required of all physically and
financially able Muslims once in their life. The pilgrimage rite begins a few months
after Ramadan, on the 8th day of the last month of the Islamic year of Dhul-Hijjah,
and ends on the 13th day. Mecca is the center towards which the Muslims converge
once a year, meet and refresh in themselves the faith that all Muslims are equal and
deserve the love and sympathy of others, irrespective of their race or ethnic origin.
The racial harmony fostered by Hajj is perhaps best captured by Malcolm X on his
historic pilgrimage:<br><br>
‘Every one of the thousands at the airport, about to leave for Jeddah, was dressed this
way. You could be a king or a peasant and no one would know. Some powerful
personages, who were discreetly pointed out to me, had on the same thing I had on.
Once thus dressed, we all had begun intermittently calling out “Labbayka!
(Allahumma) Labbayka!” (At your service, O Lord!) Packed in the plane were white,
black, brown, red, and yellow people, blue eyes and blond hair, and my kinky red hair
- all together, brothers! All honoring the same God, all in turn giving equal honor to
each other . . . <br><br>
That is when I first began to reappraise the ‘white man’. It was when I first began to
perceive that ‘white man’, as commonly used, means complexion only secondarily;
primarily it described attitudes and actions. In America, ‘white man’ meant specific
attitudes and actions toward the black man, and toward all other non-white men. But
in the Muslim world, I had seen that men with white complexions were more
genuinely brotherly than anyone else had ever been. That morning was the start of a
radical alteration in my whole outlook about ‘white’ men.<br><br>
There were tens of thousands of pilgrims, from all over the world. They were of all
colors, from blue-eyed blonds to black-skinned Africans. But we were all
participating in the same ritual displaying a spirit of unity and brotherhood that my
experiences in America had led me to believe never could exist between the white and
the non-white... America needs to understand Islam, because this is the one religion
that erases from its society the race problem. Throughout my travels in the Muslim
world, I have met, talked to, and even eaten with people who in America would have
been considered white - but the ‘white’ attitude was removed from their minds by the
religion of Islam. I have never before seen sincere and true brotherhood practiced by
all colors together, irrespective of their color.”<br><br>
Thus the pilgrimage unites the Muslims of the world into one international fraternity.
More than two million persons perform the Hajj each year, and the rite serves as a
unifying force in Islam by bringing followers of diverse backgrounds together in
worship. In some Muslim societies, once a believer has made the pilgrimage, he is often labeled with the title ‘hajji’ ; this, however, is a cultural, rather than religious
custom. Finally, the Hajj is a manifestation of the belief in the unity of God - all the
pilgrims worship and obey the commands of the One God.<br><br>
At certain stations on the caravan routes to Mecca, or when the pilgrim passes the
point nearest to those stations, the pilgrim enters the state of purity known as ihram.
In this state, the certain ‘normal’ actions of the day and night become impermissible
for the pilgrims, such as covering the head, clipping the fingernails, and wearing
normal clothing in regards to men. Males remove their clothing and don the garments
specific to this state of ihram, two white seamless sheets that are wrapped around the
body. All this increases the reverence and sanctity of the pilgrimage, the city of
Mecca, and month of Dhul-Hijjah. There are 5 stations, one on the coastal plains
northwest of Mecca towards Egypt and one south towards Yemen, while three lie
north or eastwards towards Medina, Iraq and al-Najd. The simple garb signifies the
equality of all humanity in God’s sight, and the removal of all worldly affections.
After entering the state of ihram, the pilgrim proceeds to Mecca and awaits the start of
the Hajj. On the 7th of Dhu al-Hijjah the pilgrim is reminded of his duties, and at the
commence of the ritual, which takes place between the 8th and the 12th days of the
month, the pilgrim visits the holy places outside Mecca - Arafah, Muzdalifah, and
Minaa - and sacrifices an animal in commemoration of Abraham’s sacrifice. The
pilgrim then shortens or shaves their head, and, after throwing seven stones at specific
pillars at Minaa on three or four successive days, and heads for the central mosque
where he walks seven times around the sacred sanctuary, or Kaaba, in the Great
Mosque, and ambulates, walking and running, seven times between the two small
hills of Mt. Safaa and Mt. Marwah. Discussing the historical or spiritual significance
of each rite is beyond the scope of this introductory article.<br><br>
Apart from Hajj, the “minor pilgrimage” or umrah is undertaken by Muslims during
the rest of the year. Performing the umrah does not fulfill the obligation of Hajj. It is
similar to the major and obligatory Islamic pilgrimage (hajj), and pilgrims have the
choice of performing the umrah separately or in combination with the Hajj. As in the
Hajj, the pilgrim begins the umrah by assuming the state of ihram. They enter Mecca
and circle the sacred shrine of the Kaaba seven times. He may then touch the Black
Stone, if he can, pray behind the Maqam Ibrahim, drink the holy water of the Zamzam
spring. The ambulation between the hills of Safa and Marwah seven times and the
shortening or shaving of the head complete the umrah.
