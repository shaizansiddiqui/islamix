The Shahada is the Muslim profession of faith and the first of the ‘Five Pillars’ of
Islam. The word shahada in Arabic means ‘testimony.’ The shahada is to testify to
two things:<br><br>
<b>(a) Nothing deserves worship except God (Allah).<br>
(b) Muhammad is the Messenger of God (Allah).</b><br>
A Muslim is simply one who bears witness and testifies that "nothing deserves
worship except God and Muhammad is the messenger of God." One becomes a
Muslim by making this simple declaration.
It must be recited by every Muslim at least once in a lifetime with a full understanding
of its meaning and with an assent of the heart. Muslims say this when they wake up
in the morning, and before they go to sleep at night. It is repeated five times in the
call to prayer in every mosque. A person who utters the shahada as their last words in
this life has been promised Paradise.<br>
Many people ignorant of Islam have misconceived notions about the Allah, used by
Muslims to denote God. Allah is the proper name for God in Arabic, just as "Elah", or
often "Elohim", is the proper name for God in Aramaic mentioned in the Old
Testament. Allah is also His personal name in Islam, as "YHWH" is His personal
name in Judaism. However, rather than the specific Hebrew denotation of "YHWH"
as "He Who Is", in Arabic Allah denotes the aspect of being "The One True Deity
worthy of all worship". Arabic speaking Jews and Christians also refer to the
Supreme Being as Allah.
<br><br>
<b>(a) Nothing deserves worship except God (Allah).</b>
The first part of this testimony states that God has the exclusive right to be
worshipped inwardly and outwardly, by one’s heart and limbs. In Islamic doctrine,
not only can no one be worshipped apart from Him, absolutely no one else can be
worshipped along with Him. He has no partners or associates in worship. Worship,
in its comprehensive sense and all its aspects, is for Him alone. God’s right to be
worshipped is the essential meaning of Islam’s testimony of faith: Lā ‘ilāha ‘illā Allāh.
A person becomes Muslim by testifying to the divine right to worship. It is the crux
of Islamic belief in God, even all of Islam. It is considered the central message of all
prophets and messengers sent by God - the message of Abraham, Isaac, Ishmael,
Moses, the Hebrew prophets, Jesus, and Muhammad, may the mercy and blessings of
God be upon them. For instance, Moses declared:<br><br>
<b>"Hear, O Israel The Lord our God is one Lord." (Deuteronomy 6:4)."</b><br>
Jesus repeated the same message 1500 years later when he said no one else can be
worshipped along with Him. He has no partners or associates in worship. Worship,
in its comprehensive sense and all its aspects, is for Him alone.:<br><br>
<b>"The first of all the commandments is, "Hear, O Israel; the Lord our God is one
Lord." (Mark 12:29)</b><br>
Finally, the call of Muhammad, some 600 years after Jesus, reverberated across the
hills of Mecca, ‘And your God is One God: there is no god but He.’ (Quran 2:163).
They all declared clearly:<br><br>
<b>"Worship God! You have no other god but Him." (Quran 7:59, 7:73; 11:50,
11:84; 23:32)</b><br>
But by a mere verbal profession alone, one does not become a complete Muslim. To
become a complete Muslim one has to fully carry out in practice the instruction given
by Prophet Muhammad as ordained by God. This brings us to the second part of the
testimony.<br><br>
<b>(b) Muhammad is the Messenger of God (Allah).</b>
Muhammad was born in Mecca in Arabia in the year 570 CE. His ancestry goes back
to Ishmael, a son of Prophet Abraham. The second part of the confession of faith
asserts that he is not only a prophet but also a messenger of God, a higher role also
played by Moses and Jesus before him. Like all prophets before him, he was a human
being, but chosen by God to convey His message to all humanity rather than one tribe
or nation from among the many that exist. For Muslims, Muhammad brought the last
and final revelation. In accepting Muhammad as the "last of the prophets," they
believe that his prophecy confirms and completes all of the revealed messages,
beginning with that of Adam. In addition, Muhammad serves as the preeminent role
model through his life example. The believer’s effort to follow Muhammad’s
example reflects the emphasis of Islam on practice and action.<br>
