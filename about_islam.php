<p style="color: black;font-size: 20px ;text-align: justify;
display: flex;
flex-wrap: wrap;
align-content: center;margin-left: 100px;margin-right: 100px;">The word “Islam”
means “submission to the will of God.”
Followers of Islam are called Muslims.
Muslims are monotheistic and worship one, all-knowing God, who in Arabic is
known as Allah.
Followers of Islam aim to live a life of complete submission to Allah. They believe
that nothing can happen without Allah’s permission, but humans have free will.
Islam teaches that Allah’s word was revealed to the prophet Muhammad through the
angel Gabriel.
Muslims believe several prophets were sent to teach Allah’s law. They respect some
of the same prophets as Jews and Christians, including Abraham, Moses, Noah and
Jesus. Muslims contend that Muhammad was the final prophet.
Mosques are places where Muslims worship.
Some important Islamic holy places include the Kaaba shrine in Mecca, the Al-Aqsa
mosque in Jerusalem, and the Prophet Muhammad’s mosque in Medina.
The Quran (or Koran) is the major holy text of Islam. The Hadith is another important
book. Muslims also revere some material found in the Judeo-Christian Bible.
Followers worship Allah by praying and reciting the Quran. They believe there will
be a day of judgment, and life after death.
A central idea in Islam is “jihad,” which means “struggle.” While the term has been
used negatively in mainstream culture, Muslims believe it refers to internal and
external efforts to defend their faith. Although rare, this can include military jihad if a
“just war” is needed.
Muhammad
The prophet Muhammad, sometimes spelled Mohammed or Mohammad, was born in
Mecca, Saudi Arabia, in 570 A.D. Muslims believe he was the final prophet sent by
God to reveal their faith to mankind.
According to Islamic texts and tradition, an angel named Gabriel visited Muhammad
in 610 A.D. while he was meditating in a cave. The angel ordered Muhammad to
recite the words of Allah.
Muslims believe that Muhammad continued to receive revelations from Allah
throughout the rest of his life.
Starting in about 613, Muhammad began preaching throughout Mecca the messages
he received. He taught that there was no other God but Allah and that Muslims should
devote their lives to this God.
Hijra
In 622, Muhammad traveled from Mecca to Medina with his supporters. This journey
became known as the Hijra (also spelled Hegira or Hijrah), and marks the beginning
of the Islamic calendar.Some seven years later, Muhammad and his many followers returned to Mecca and
conquered the region. He continued to preach until his death in 632.
Abu Bakr
After Muhammad’s passing, Islam began to spread rapidly. A series of leaders,
known as caliphs, became successors to Muhammad. This system of leadership,
which was run by a Muslim ruler, became known as a caliphate.
The first caliph was Abu Bakr, Muhammad’s father-in-law and close friend.
Abu Bakr died about two years after he was elected and was succeeded in 634 by
Caliph Umar, another father-in-law of Muhammad.
Caliphate System
When Umar was assassinated six years after being named caliph, Uthman,
Muhammad’s son-in-law, took the role.
Uthman was also killed, and Ali, Muhammad’s cousin and son-in-law, was selected
as the next caliph.
During the reign of the first four caliphs, Arab Muslims conquered large regions in
the Middle East, including Syria, Palestine, Iran and Iraq. Islam also spread
throughout areas in Europe, Africa, and Asia.
The caliphate system lasted for centuries and eventually evolved into the Ottoman
Empire, which controlled large regions in the Middle East from about 1517 until 1917,
when World War I ended the Ottoman reign.
Sunnis and Shiites
When Muhammad died, there was debate over who should replace him as leader. This
led to a schism in Islam, and two major sects emerged: the Sunnis and the Shiites.
Sunnis make up nearly 90 percent of Muslims worldwide. They accept that the first
four caliphs were the true successors to Muhammad.
Shiite Muslims believe that only the caliph Ali and his descendants are the real
successors to Muhammad. They deny the legitimacy of the first three caliphs. Today,
Shiite Muslims have a considerable presence in Iran, Iraq and Syria.
Other Types of Islam
Other, smaller Muslim denominations within the Sunni and Shiite groups exist. Some
of these include:
Wahhabi: This Sunni sect, made up of members of the Tameem tribe in Saudi Arabia,
was founded in the 18th century. Followers observe an extremely strict interpretation
of Islam that was taught by Muhammad bin Abd al-Wahhab.
Alawite: This Shiite form of Islam is prevalent in Syria. Followers hold similar beliefs
about the caliph Ali but also observe some Christian and Zoroastrian holidays.Nation of Islam: This mostly African-American, Sunni sect was founded in the 1930s
in Detroit, Michigan.
Kharijites: This sect broke from the Shiites after disagreeing over how to select a new
leader. They are known for radical fundamentalism, and today are called Ibadis.
Quran
Islam Quran
The Quran.
