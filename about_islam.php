Islamic Calendar
The Islamic calendar, also called the Hijra calendar, is a lunar calendar used in
Islamic religious worship. The calendar began in the year 622 A.D., celebrating the
journey of Muhammad from Mecca to Medina.
The Islamic calendar indicates the proper days of Islamic holidays and celebrations,
including the period of fasting and prayer known as Ramadan, which occurs during
the ninth month of the calendar.
Islam Symbols
As in many religions, there is no single image or symbol of Islam that is universally
accepted by all Muslims worldwide.
The crescent moon and star has been adopted in some predominantly Muslim
countries as a symbol of Islam, though the crescent moon and star image is believed
to pre-date Islam and was originally a symbol of the Ottoman Empire.
In some other applications, such as the International Red Cross and Red Crescent
humanitarian aid movement, a red crescent indicates that followers of Islam are
respected and treated accordingly.The color green is also sometimes associated with Islam, as it was reportedly a
favorite color of Muhammad's and is often featured prominently in the flags of
predominantly Muslim countries.
Five Pillars of Islam
Muslims follow five basic pillars that are essential to their faith. These include:
Shahada: to declare one’s faith in God and belief in Muhammad
Salat: to pray five times a day (at dawn, noon, afternoon, sunset, and evening)
Zakat: to give to those in need
Sawm: to fast during Ramadan
Hajj: to make a pilgrimage to Mecca at least once during a person’s lifetime if the
person is able
Sharia Law
Islam’s legal system is known as Sharia Law. This faith-based code of conduct directs
Muslims on how they should live in nearly every aspect of their lives.
Sharia law requires men and women to dress modestly. It also outlines marriage
guidelines and other moral principles for Muslims.
If crimes are committed, Sharia law is known for its harsh punishments. For example,
the punishment for theft is amputating a person’s hand. Adultery can carry the penalty
of death by stoning. However, many Muslims do not support such extreme measures.
Muslim Prayer
The prophet Muhammad is credited with building the first mosque in the courtyard of
his house in Medina. Mosques today follow some of the same principles he
established in 622 A.D.
Muslim prayer is often conducted in a mosque's large open space or outdoor courtyard.
A mihrab is a decorative feature or niche in the mosque that indicates the direction to
Mecca, and therefore the direction to face during prayer.
Men and women pray separately, and Muslims may visit a mosque five times a day
for each of the prayer sessions. In addition to hosting prayers, mosques often function
as public gathering places and social centers.
Muslim Holidays
The two major Muslim holidays are:
Eid al-Adha: celebrates the Prophet Abraham’s willingness to sacrifice his son for
Allah.
Eid al-Fitr: marks the end of Ramadan—the Islamic holy month of fasting.
Muslims also celebrate other holidays, such as the Islamic New Year and the birth of
Muhammad.
Islam Today In recent years, Islam’s supposed association with terrorism and mass murder has
sparked a political debate in many countries. The controversial term “radical Islam”
has become a well-known label to describe the religion’s connection to acts of
violence.
While some Muslims use their faith to justify terrorism, the vast majority do not. In
fact, Muslims are frequently victims of violence themselves.
Recent surveys have found that in countries with high Muslim populations, the
majority of Muslims have overwhelmingly negative views of terrorist groups like
ISIS.
While Muslims aim to clear up misconceptions about their faith, the religion
continues to spread rapidly. Today, Islam is the world’s fastest growing religion.
Experts predict Islam will surpass Christianity as the largest religion by the end of the
century.
